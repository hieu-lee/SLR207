package rs;

public class MinMaxFreq {
    private final int theMinFreq;
    private final int theMaxFreq;

    public MinMaxFreq(int aMinFreq, int aMaxFreq) {
        theMinFreq = aMinFreq;
        theMaxFreq = aMaxFreq;
    }

    public int getMinFreq() {
        return theMinFreq;
    }

    public int getMaxFreq() {
        return theMaxFreq;
    }
}
