package rs;

public class FreqCount {
    private final int theFreq;
    private final int theCount;

    public FreqCount(int aFreq, int aCount) {
        theFreq = aFreq;
        theCount = aCount;
    }

    public int getFreq() {
        return theFreq;
    }

    public int getCount() {
        return theCount;
    }
}
